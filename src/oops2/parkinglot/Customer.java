package oops2.parkinglot;

public class Customer {
    String name;
    int customerId;
    long phoneNo;
    int age;

    public Customer(String name, int customerId, long phoneNo, int age) {
        this.name = name;
        this.customerId = customerId;
        this.phoneNo = phoneNo;
        this.age = age;
    }

}

