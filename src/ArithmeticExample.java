 class ArithmeticExample {
     public static void main(String[] args) {
          int number1 = 10;
          int number2 = 2;

          int sumOfTwoNumbers = number1+number2;
          int diffOfTwoNumbers = number1-number2;
          int mulOfTwoNumbers = number1*number2;
          int divOfTwoNumbers = number1/number2;
          System.out.println(sumOfTwoNumbers);
          System.out.println(diffOfTwoNumbers);
          System.out.println(mulOfTwoNumbers);
          System.out.println(divOfTwoNumbers);
     }
}
